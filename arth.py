def addition(firvar, secvar):
    """
    it is used to perform addition of two numbers

    **params** **types** **return** **rtype**

    :param firvar: first argument
    :type firvar: int
    :praram secvar: second argument
    :type secvar: int
    :return: addition of two numbers
    :rtype: int
    """
    return firvar+secvar

def mul(firvar, secvar):
    return firvar*secvar

def subs(firvar, secvar):
    return firvar-secvar

def div(a, b):
    return a/b
def arth(a, b):
    return a*b+a+b
